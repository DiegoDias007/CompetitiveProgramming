void solve() {
    int n;
    cin >> n;
    int ans = 0;
    for (int x = 0; x <= n; x++) {
        for (int y = 0; y <= n; y++) {
            int a = 2*x + 4*y;
            if (a == n) {
                ans++;
            }
        }
    }
    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    
    int t;
    cin >> t; 
    while (t--) {
        solve();
    }
    
    return 0;
}   